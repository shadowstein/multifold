      <h3>Ergonomic Office Design</h3>
      <div id="list_content">
        <p>Designing office spaces for maximum efficiency and comfort. 
        Ergonomic office designs make it easier for the worker to do various 
        tasks while sitting down or standing up by limiting the required 
        range of motion needed to get work done.
    	</p>
      </div>
      <h3>Technocentric Office Design</h3>
      <div id="list_content">
          <p>Putting technology at the forefront of the office space, 
          techno centric office designs desegregates the workplace through networking. 
          Making vital resources available with a simple click is essential to 
          maintaining high efficiency in the workplace while keeping the work 
          environment clutter-free. By strategically placing computers, equipment, phones, 
          etc., and creating an almost wireless environment, vital resources are brought 
          to the user for a more convenient and efficient office.
          </p>
