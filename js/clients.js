function showToggleDiv() {
  if (document.getElementById('govt_toggleDivContents').style.display == 'none') {
    document.getElementById('govt_toggleDivContents').style.display = 'block';
  } else {
    document.getElementById('govt_toggleDivContents').style.display = 'none';
  }
}
